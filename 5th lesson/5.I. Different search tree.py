def search_tree_counter(n):
    if n == 0:
        return 1
    counter = 0
    for i in range(1, n+1):
        left_nodes = i - 1
        right_nodes = n - i
        counter += search_tree_counter(left_nodes) * search_tree_counter(right_nodes)
    return counter


